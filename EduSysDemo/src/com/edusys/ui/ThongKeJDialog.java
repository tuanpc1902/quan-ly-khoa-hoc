    private static HSSFCellStyle createStyleForTitle(HSSFWorkbook workbook) {
        HSSFFont font = workbook.createFont();
        font.setBold(true);
        HSSFCellStyle style = workbook.createCellStyle();
        style.setFont(font);
        return style;
    }

    void XuatExcelBangDiem() throws FileNotFoundException, IOException {
        HSSFWorkbook workbook = new HSSFWorkbook();
        HSSFSheet sheet = workbook.createSheet("Quản Lý Điểm Học Viên");

        HocVienDAO hvdao = new HocVienDAO();
        NguoiHocDAO nhdao = new NguoiHocDAO();

        List<NguoiHoc> list = nhdao.selectAll();
        List<HocVien> list1 = hvdao.selectAll();

        int rownum = 2;

        int rownum1 = 0;
        Cell cell;
        Row row;
        Row row1;

        //
        HSSFCellStyle style = createStyleForTitle(workbook);

        row = sheet.createRow(rownum);
        row1 = sheet.createRow(rownum1);

        cell = row1.createCell(2, CellType.STRING);
        cell.setCellValue("Quản Lý Điểm Học Viên");
        cell.setCellStyle(style);

        // Mã Nhân Viên
        cell = row.createCell(0, CellType.STRING);
        cell.setCellValue("Mã HV");
        cell.setCellStyle(style);
        // Mật Khẩu
        cell = row.createCell(1, CellType.STRING);
        cell.setCellValue("MÃ NH");
        cell.setCellStyle(style);
        // Họ Tên
        cell = row.createCell(2, CellType.STRING);
        cell.setCellValue("HỌ TÊN");
        cell.setCellStyle(style);
        // Email
        cell = row.createCell(3, CellType.STRING);
        cell.setCellValue("ĐIỂM");
        cell.setCellStyle(style);
        // Data
        for (HocVien hv : list1) {
            rownum++;
            row = sheet.createRow(rownum);

            // getMaNV 
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue(hv.getMaHV());
            // getMatKhau
            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue(hv.getMaNH());
            //
            String hoten = nhdao.selectById(hv.getMaNH()).getHoTen();
            cell = row.createCell(2, CellType.STRING);
            cell.setCellValue(hoten);
            //
            cell = row.createCell(3, CellType.STRING);
            cell.setCellValue(hv.getDiem());
        }

        File file = new File("C:/EduSys/quanlydiemhocvien.xls");
        file.getParentFile().mkdirs();

        FileOutputStream outFile = new FileOutputStream(file);
        workbook.write(outFile);
        System.out.println("Created file: " + file.getAbsolutePath());
    }
